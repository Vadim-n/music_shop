<?php


namespace App\Helpers;


trait AliasTrait
{
    /**
     * @param string $str
     * @return string|string[]|null
     */
    private function getAlias(string $str): string
    {
        $converter = array(
            'а' => 'a', 'б' => 'b', 'в' => 'v',
            'г' => 'g', 'д' => 'd', 'е' => 'e',
            'ё' => 'e', 'ж' => 'zh', 'з' => 'z',
            'и' => 'i', 'й' => 'y', 'к' => 'k',
            'л' => 'l', 'м' => 'm', 'н' => 'n',
            'о' => 'o', 'п' => 'p', 'р' => 'r',
            'с' => 's', 'т' => 't', 'у' => 'u',
            'ф' => 'f', 'х' => 'h', 'ц' => 'c',
            'ч' => 'ch', 'ш' => 'sh', 'щ' => 'sch',
            'ь' => '', 'ы' => 'y', 'ъ' => '',
            'э' => 'e', 'ю' => 'yu', 'я' => 'ya',

            'А' => 'A', 'Б' => 'B', 'В' => 'V',
            'Г' => 'G', 'Д' => 'D', 'Е' => 'E',
            'Ё' => 'E', 'Ж' => 'Zh', 'З' => 'Z',
            'И' => 'I', 'Й' => 'Y', 'К' => 'K',
            'Л' => 'L', 'М' => 'M', 'Н' => 'N',
            'О' => 'O', 'П' => 'P', 'Р' => 'R',
            'С' => 'S', 'Т' => 'T', 'У' => 'U',
            'Ф' => 'F', 'Х' => 'H', 'Ц' => 'C',
            'Ч' => 'Ch', 'Ш' => 'Sh', 'Щ' => 'Sch',
            'Ь' => '', 'Ы' => 'Y', 'Ъ' => '',
            'Э' => 'E', 'Ю' => 'Yu', 'Я' => 'Ya',
        );
        $str = strtr($str, $converter);
        $str = strtolower($str);
        $str = preg_replace('~[^-a-z0-9_]+~u', '-', $str);
        $str = trim($str, "-");
        return $str;
    }

    /**
     * @param string $modelClass
     * @param string $alias
     * @param int $categoryId
     * @param int $num
     * @return string
     */
    public function checkAliasExistance(string $modelClass, string $alias, ?int $entityId = null, int $num = 1): string
    {
        $findAlias = $num > 1 ? $alias . "-$num" : $alias;
        $existedCategory = $modelClass::where('alias', $findAlias)
            ->when($entityId !== null, function ($query) use($entityId) {
                $query->where('id', '!=', $entityId);
            })
            ->first();

        if ($existedCategory) {
            return $this->checkAliasExistance($modelClass, $alias, $entityId, $num + 1);
        } else {
            return $findAlias;
        }
    }
}
